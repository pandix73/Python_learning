def count_gold(pyramid):
    list_pyramid = []
    for layer in pyramid:
        list_pyramid.append(list(layer))
    for i in reversed(range(len(list_pyramid)-1)):
        for j in range(len(list_pyramid[i])):
            list_pyramid[i][j] += max(list_pyramid[i+1][j], list_pyramid[i+1][j+1]);
    return list_pyramid[0][0]
